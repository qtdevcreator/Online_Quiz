package com.software.onlexam;

public class Quiz {
	private int quizno;
	private int noteams;
	private int marksperq;
	private int negscore;
	private int noques;
	private int passmarks;
	private int maxscore;
	private String quizname;
	public int getQuizno() {
		return quizno;
	}
	public void setQuizno(int quizno) {
		this.quizno = quizno;
	}
	public int getNoteams() {
		return noteams;
	}
	public void setNoteams(int noteams) {
		this.noteams = noteams;
	}
	public int getMarksperq() {
		return marksperq;
	}
	public void setMarksperq(int marksperq) {
		this.marksperq = marksperq;
	}
	public int getNegscore() {
		return negscore;
	}
	public void setNegscore(int negscore) {
		this.negscore = negscore;
	}
	public int getNoques() {
		return noques;
	}
	public void setNoques(int noques) {
		this.noques = noques;
	}
	public int getPassmarks() {
		return passmarks;
	}
	public void setPassmarks(int passmarks) {
		this.passmarks = passmarks;
	}
	public int getMaxscore() {
		return maxscore;
	}
	public void setMaxscore(int maxscore) {
		this.maxscore = maxscore;
	}
	public String getQuizname() {
		return quizname;
	}
	public void setQuizname(String quizname) {
		this.quizname = quizname;
	}
	
	
}
